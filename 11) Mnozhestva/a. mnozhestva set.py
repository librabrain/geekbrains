# Mnozhestvo - SET(set)
# Neuporyadochenniye kollekcii, soderzhashiye ne povtoryayushiesya elementyi
# Vo mnozhestve ne mozhet biyt 2-h odinakoviyh elementov
# cities = {'Las Vegas', 'Paris', 'Moscow'}

cities = ['Las Vegas', 'Paris', 'Moscow', 'Paris', 'Moscow']
print(type(cities))
print(cities)

cities = set(cities)
print(cities)
print(type(cities))

cities = {'Las Vegas', 'Paris', 'Moscow'}
print(cities)

